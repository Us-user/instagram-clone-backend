namespace Domain.Enums;

/// <summary>
/// Статус связи подписки. Подписка на публичный аккаунт создаётся сразу как
/// <see cref="Accepted"/>; на приватный — как <see cref="Pending"/> (запрос на подписку),
/// пока владелец не одобрит. Числовые коды фиксированы (0/1) — существующие связи базы
/// после миграции считаются <see cref="Accepted"/>.
/// </summary>
public enum FollowStatus
{
    /// <summary>Запрос на подписку ожидает одобрения владельца приватного аккаунта.</summary>
    Pending = 0,

    /// <summary>Подписка активна.</summary>
    Accepted = 1
}
